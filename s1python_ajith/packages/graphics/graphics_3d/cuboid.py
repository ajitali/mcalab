

def area_cuboid():
	l=float(input("enter the length of cuboid: "))
	b=float(input("enter the breadth of cuboid: "))
	h=float(input("enter the height of cuboid: "))

	print("Surface area of cuboid is:",2*((l*b)+(b*h)+(h*l)))

def volume_cuboid():
	l=float(input("enter the length of cuboid: "))
	b=float(input("enter the breadth of cuboid: "))
	h=float(input("enter the height of cuboid: "))
	print("Volume of cuboid is:",l*b*h)


